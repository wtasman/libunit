#include "libft.h"

char	*ft_strrev(char *s)
{
	size_t	len;
	size_t	i;

	i = 0;
	len = ft_strlen(s);
	len--;
	while (i < len)
	{
		ft_swap(&s[i], &s[len]);
		i++;
		len--;
	}
	return (s);
}
